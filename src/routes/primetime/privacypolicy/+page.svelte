<h3>1. Introduction</h3>

PrimeTime is a Chrome extension designed to help users factorize numbers they select on webpages. This privacy policy explains how we handle user data and protect your privacy.

<hr class="my-2">

<h3>2. Information We Collect</h3>

PrimeTime does not collect, store, or share any personal information or browsing data. The extension operates entirely on your local device and does not transmit data to any external servers or services.

    <hr class="my-2">

<h3>3. How We Use Data</h3>

PrimeTime processes selected numbers directly within your browser to provide their factorization. The extension:
<ul>
<li>    - Only processes the selected number on the current webpage.</li>
<li>    - Does not access, track, or store any other data.</li>
</ul>
    <hr class="my-2">

<h3>4. Data Sharing</h3>

PrimeTime does not share, sell, or transmit any data with third parties. All operations occur locally on your device.

    <hr class="my-2">

<h3>5. Security</h3>

Since PrimeTime does not collect or store any data, there are no risks associated with data leaks or breaches. The extension uses local-only processing to ensure your privacy.

    <hr class="my-2">

<h3>6. Third-Party Services</h3>

PrimeTime does not use any third-party services, APIs, or analytics tools. It functions entirely offline.

